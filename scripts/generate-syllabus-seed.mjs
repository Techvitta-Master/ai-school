/**
 * Generates supabase/sql/010_syllabus_seed.sql from src/data/data-ch.json
 * Run: node scripts/generate-syllabus-seed.mjs
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const jsonPath = path.join(root, 'src', 'data', 'data-ch.json');
const outPath = path.join(root, 'supabase', 'sql', '010_syllabus_seed.sql');

const j = JSON.parse(fs.readFileSync(jsonPath, 'utf8'));

function escSqlString(s) {
  return String(s).replace(/'/g, "''");
}

let sql = `-- 010_syllabus_seed.sql (generated by scripts/generate-syllabus-seed.mjs)
-- Inserts syllabus themes and chapters from data-ch.json

begin;

`;

for (const th of j.themes) {
  sql += `insert into public.syllabus_themes (theme_code, title, domain)
values ('${escSqlString(th.theme)}', '${escSqlString(th.title)}', '${escSqlString(th.domain)}')
on conflict (theme_code) do update set title = excluded.title, domain = excluded.domain;

`;

  for (const ch of th.chapters) {
    const topicsLiteral = escSqlString(JSON.stringify(ch.topics));
    sql += `insert into public.syllabus_chapters (theme_id, chapter_number, title, topics)
select id, ${ch.chapter_number}, '${escSqlString(ch.title)}', '${topicsLiteral}'::jsonb
from public.syllabus_themes where theme_code = '${escSqlString(th.theme)}'
on conflict (theme_id, chapter_number) do update set title = excluded.title, topics = excluded.topics;

`;
  }
}

sql += `commit;
`;

fs.writeFileSync(outPath, sql, 'utf8');
console.log('Wrote', outPath);
